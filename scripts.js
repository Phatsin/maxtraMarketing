function menuToggle(id) {
  var x = document.getElementsByClassName("sub-menu");
  for(var i=0; i<x.length; i++){
      x[i].style.display="none";
      x[i].children[0].value="welcome1";
  }
  var xx = document.getElementById(id);
  xx.style.display="block";

  var y = document.getElementsByClassName("menuButton");
  for(var i=0; i<y.length; i++){
    y[i].style.background="";
  }
  var yy = document.getElementById(id+"-button");
  yy.style.background="green";

} 
function showContent(select) {
  if(data[select]===undefined){
    document.getElementById("textarea").value="ไม่มีข้อมูล";    
    document.getElementById("text").innerText="ไม่มีข้อมูล";
    const parent = document.getElementById("image")
    while (parent.firstChild) {
      parent.firstChild.remove()
    }
    var img = document.createElement("img");
    img.src = "images/contact.jpg";
    var src = document.getElementById("image");
    src.appendChild(img);
  }else{
    document.getElementById("text").innerHTML=urlify(data[select].text);
    document.getElementById("textarea").value=data[select].text;
    const parent = document.getElementById("image")
    while (parent.firstChild) {
        parent.firstChild.remove()
    }
    for (let i=0; i<data[select].image.length; i++) {
      var img = document.createElement("img");
      img.src = data[select].image[i];
      var src = document.getElementById("image");
      src.appendChild(img);
    } 
  }
}
